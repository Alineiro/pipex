#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * -1;
	}
	if (n > 9)
		ft_putnbr_fd((n / 10), fd);
	ft_putchar_fd(((n % 10) + '0'), fd);
}
//WRITES N (TYPE INT) TO THE GIVEN FILE DESCRIPTOR (TYPE INT). WORKS LIKE A 
//NORMAL PUTNBR BUT WRITES TO FD INSTEAD OF 1 BY DEFAULT. AN FD TELLS THE
//SYSTEM FROM WHERE IT SHOULD READ OR WRITE INFORMATION.
